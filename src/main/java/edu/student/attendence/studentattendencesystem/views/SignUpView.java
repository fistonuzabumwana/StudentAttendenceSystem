        private void signUpButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_signUpButtonActionPerformed
               int studentId = Integer.parseInt(usernameTextField.getText());
               String firstName = fnameTextField.getText();
                String lastName = lnameTextField.getText();
                String email = emailTextField.getText();
                String password = passwordTextField.getText();
                String confirmPassword = confirmPasswordTextField.getText();
                String role;
                if (studentRadioButton.isSelected()){
                         role = "Student";
                }else if(staffRadioButton.isSelected()){
                         role = "Staff";
                } else if (adminRadioButton.isSelected()) {
                         role = "Admin";
                } else {
                         role = null;
                }

                String query = "INSERT INTO SAS.Users(UserID, FirstName, LastName, Email, Password, Role) VALUES (?, ?, ?, ?, ?, ?)";

                        try (Connection con = DriverManager.getConnection(this.url, this.username, this.password);
                             PreparedStatement insert = con.prepareStatement(query)) {

                                insert.setInt(1, studentId);
                                insert.setString(2, firstName);
                                insert.setString(3, lastName);
                                insert.setString(4, email);
                                insert.setString(5, password);
                                insert.setString(6, role);


                                int rowsAffected = insert.executeUpdate();

                                if (rowsAffected > 0) {
                                        usernameTextField.setText(null);
                                        fnameTextField.setText(null);
                                        lnameTextField.setText(null);
                                        emailTextField.setText(null);
                                        passwordTextField.setText(null);
                                        confirmPasswordTextField.setText(null);
                                        adminRadioButton.setSelected(false);
                                        staffRadioButton.setSelected(false);
                                        studentRadioButton.setSelected(false);
                                        accessLabel.setVisible(false);
                                        accessPasswordField.setVisible(false);
                                        showMessageDialog(this, "User Registered Successfully!!");
                                        LoginView login = new LoginView();
                                        login.setVisible(true);
                                        dispose();
                                } else {
                                        showMessageDialog(this, "User not Registered due to unexpected error.");
                                }

                        } catch (SQLException e) {
                                Logger.getLogger(SignUpView.class.getName()).log(Level.SEVERE, null, e);
                                showMessageDialog(this, "Database Error: " + e.getMessage());
                        }
    }

