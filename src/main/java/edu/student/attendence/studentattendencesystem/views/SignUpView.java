        private void getAccess(){
                int pass = Integer.parseInt(accessPasswordField.getText());
                int digits = pass == 0 ? 1 : (int) Math.log10(Math.abs(pass)) + 1;

                if (pass == 4444 && digits == 4){
                        staffRadioButton.setSelected(false);
                        adminRadioButton.setSelected(true);
                        studentRadioButton.setSelected(false);
                }else if(pass == 3333 && digits == 4) {
                        adminRadioButton.setSelected(false);
                        staffRadioButton.setSelected(true);
                        studentRadioButton.setSelected(false);
                }else {
                        adminRadioButton.setSelected(false);
                        staffRadioButton.setSelected(false);
                        studentRadioButton.setSelected(false);
                }
        }

